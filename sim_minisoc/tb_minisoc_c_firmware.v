            end else begin
                $fdisplay(log_file, "[FAIL] No GPIO Activity detected (Increase timeout if needed)");
                tests_failed = tests_failed + 1;
            end
            
            $fdisplay(log_file, "Total Passed: %0d, Total Failed: %0d", (tests_failed == 0 ? 2 : 0), tests_failed);
        end
    endtask


