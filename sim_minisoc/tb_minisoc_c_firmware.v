    assign gpio_monitor[1] = gpio1_io;
    assign gpio_monitor[2] = gpio2_io;
    assign gpio_monitor[3] = gpio3_io;
    assign gpio_monitor[4] = gpio4_io;
    assign gpio_monitor[5] = gpio5_io;
