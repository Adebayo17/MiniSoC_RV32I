    assign gpio0_io = gpio_dir[0] ? 1'bz : gpio_stim[0];
    assign gpio1_io = gpio_dir[1] ? 1'bz : gpio_stim[1];
    assign gpio2_io = gpio_dir[2] ? 1'bz : gpio_stim[2];
